package com.itbank.big;

public class TodayDoing {
	String title;
	int time;
	String location;
	
	public TodayDoing(String title, int time, String location) {
		this.title = title;
		this.time = time;
		this.location = location;
	}

	@Override
	public String toString() {
		return "TodayDoing [title=" + title + ", time=" + time + ", location=" + location + "]";
	}
	
	
	
	
	
	
	
	
	
}

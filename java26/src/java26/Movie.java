package java26;

public class Movie {
	private String id;
	private String name;
	private String tel;
	private int hour;
	private int seat;

	public Movie(String id, String name, String tel, int hour, int seat) {
		this.id = id;
		this.name = name;
		this.tel = tel;
		this.hour = hour;
		this.seat = seat;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public String getId() {
		return id;
	}

	public int getHour() {
		return hour;
	}

	public int getSeat() {
		return seat;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	

}
